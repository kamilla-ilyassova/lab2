x = 5
y = 3
print(x == y) # Equal operator. Returns False because 5 is not equal to 3
print(x != y) # Not equal operator. Returns True because 5 is not equal to 3
print(x > y) # Greater than operator. Returns True because 5 is greater than 3
print(x < y) # Less than operator. Returns False because 5 is not less than 3
print(x >= y) # Greater than or equal to operator. Returns True because five is greater, or equal, to 3
print(x <= y) # Less than or equal to operator. Returns False because 5 is neither less than or equal to 3
